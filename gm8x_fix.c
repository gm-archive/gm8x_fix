#define CLOSE_PATCHER \
	if (!silent) { \
		puts("Press Enter to close the patcher."); \
		getchar(); \
	} \
	exit(1);
#define puts if (!silent) puts
#define printf if (!silent) printf
#define wait() if (!silent) while (getchar() != '\n');

typedef struct Patch_t {
	int pos;
	int orig;
	int new;
} Patch;

